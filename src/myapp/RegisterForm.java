    private void registerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerMouseClicked

         int check = validateRegister();
        if(check == 1){
            

                String pass;
            try {
                pass = PasswordHasher.hashPassword(password.getText());
          
        
            dbConnector dbc = new dbConnector();
            int result = dbc.insertData("INSERT INTO tbl_user (user_Firstname, user_Lastname, user_email, username, password, user_status) "
                    + "VALUES ('"+fname.getText()+"', '"+lname.getText()+"', '"+email.getText()+"', '"+username.getText()+"', '"+pass+"', 'Pending')");
            if(result == 1){
                JOptionPane.showMessageDialog(null, "Successfully Registered!");
                    loginForm  lf = new loginForm();
                    this.dispose();
                    lf.setVisible(true);
              
                    
            }
            else{
                 System.out.println("Saving Data Failed!");
            }
              } catch (NoSuchAlgorithmException ex) {
                System.out.println(""+ex);
            }
          
            
        }else{
             JOptionPane.showMessageDialog(null, "All fields are required!");
        }

    }//GEN-LAST:event_registerMouseClicked

